    #      validation:
    #          target: path to validation dataset
    #          params:
    #              key: value
    #      test:
    #          target: path to test dataset
    #          params:
    #              key: value
    # lightning: (optional, has sane defaults and can be specified on cmdline)
    #   trainer:
    #       additional arguments to trainer
    #   logger:
    #       logger to instantiate
    #   modelcheckpoint:
    #       modelcheckpoint to instantiate
    #   callbacks:
    #       callback1:
    #           target: importpath
    #           params:
    #               key: value

    now = datetime.datetime.now().strftime("%Y-%m-%dT%H-%M-%S")

    # add cwd for convenience and to make classes in this file available when
    # running as `python main.py`
    # (in particular `main.DataModuleFromConfig`)
    sys.path.append(os.getcwd())

    parser = get_parser()

    opt, unknown = parser.parse_known_args()
    if opt.name and opt.resume:
        raise ValueError(
            "-n/--name and -r/--resume cannot be specified both."
            "If you want to resume training in a new log folder, "
            "use -n/--name in combination with --resume_from_checkpoint"
        )
    opt.resume_from_checkpoint = None
    if opt.resume:
        if not os.path.exists(opt.resume):
            raise ValueError("Cannot find {}".format(opt.resume))
        if os.path.isfile(opt.resume):
            paths = opt.resume.split("/")
            # idx = len(paths)-paths[::-1].index("logs")+1
            # logdir = "/".join(paths[:idx])
            logdir = "/".join(paths[:-2])
            ckpt = opt.resume
        else:
            assert os.path.isdir(opt.resume), opt.resume
            logdir = opt.resume.rstrip("/")
            ckpt = os.path.join(logdir, "checkpoints", "last.ckpt")

        opt.resume_from_checkpoint = ckpt
        base_configs = sorted(glob.glob(os.path.join(logdir, "configs/*.yaml")))
        opt.base = base_configs + opt.base
        _tmp = logdir.split("/")
        nowname = _tmp[-1]
    else:
        if opt.name:
            name = "_" + opt.name
        elif opt.base:
            cfg_fname = os.path.split(opt.base[0])[-1]
            cfg_name = os.path.splitext(cfg_fname)[0]
            name = "_" + cfg_name
        else:
            name = ""
        nowname = now + name + opt.postfix
        logdir = os.path.join(opt.logdir, nowname)

    ckptdir = os.path.join(logdir, "checkpoints")
    cfgdir = os.path.join(logdir, "configs")
    seed_everything(opt.seed)

    try:
        # init and save configs
        configs = [OmegaConf.load(cfg) for cfg in opt.base]
        # Omegaconf only parses args correctly if they are of the format <arg>=<value>
        # If given in format <arg>SPACE<value>, it reads it as two separate args with None value, ie <arg>:None, <val>:None
        # make unknown compliant with Omegaconf
        index = 0
        while index < len(unknown) - 1:
            if "--" in unknown[index]:
                # Allows using args of format "lightning.Trainer.<param>" as optional args
                unknown[index] = unknown[index].lstrip("--")
            if "=" not in unknown[index]:
                # allows use of <arg> <value> with Omegaconf
                unknown[index] = "=".join([unknown[index], unknown[index+1]])
                del unknown[index + 1]
            else:
                index += 1
        cli = OmegaConf.from_dotlist(unknown)
        config = OmegaConf.merge(*configs, cli)
        lightning_config = config.pop("lightning", OmegaConf.create())
        # merge trainer cli with config
        trainer_config = lightning_config.get("trainer", OmegaConf.create())
        for k in nondefault_trainer_args(opt):
            trainer_config[k] = getattr(opt, k)
        cpu = False
        if opt.hpus:
            trainer_config["devices"] = opt.hpus
            config.data.params.batch_size = opt.batch_size
            if not opt.use_lazy_mode:
                os.environ["PT_HPU_LAZY_MODE"] = "2"
            # Enable hpu dynamic shape
            try:
                import habana_frameworks.torch.hpu as hthpu
                hthpu.enable_dynamic_shape()
            except ImportError:
                print("habana_frameworks could not be loaded")
        elif not "gpus" in trainer_config:
            trainer_config["accelerator"] = "cpu"
            trainer_config["devices"] = 1
            cpu = True
        elif "gpus" in trainer_config:
            # default to ddp
            trainer_config["accelerator"] = "ddp"
            gpuinfo = trainer_config["gpus"]
            print(f"Running on GPUs {gpuinfo}")
        if opt.max_epochs is not None:
            trainer_config["max_epochs"] = opt.max_epochs
        if opt.limit_train_batches is not None:
            trainer_config["limit_train_batches"] = opt.limit_train_batches
        if opt.limit_val_batches is not None:
            trainer_config["limit_val_batches"] = opt.limit_val_batches
        if opt.val_check_interval is not None:
            trainer_config["val_check_interval"] = opt.val_check_interval
        if opt.num_nodes is not None:
            trainer_config["num_nodes"] = opt.num_nodes
        # Check if any unknown arg exists in both cmdline and config file,
        # update the param with cmdline value
        for key, value in cli.items():
            if key in trainer_config:
                trainer_config[key] = value

        trainer_opt = argparse.Namespace(**trainer_config)
        lightning_config.trainer = trainer_config
        # model
        if len(opt.ckpt_path):
            config.model.params.first_stage_config.params.ckpt_path = opt.ckpt_path

        if not opt.image_logger:
            lightning_config.callbacks.image_logger.params.disabled = True
        if not opt.hpus:
            opt.use_lazy_mode = False
            opt.hpu_graph = False
        config.model.params.hpu = (opt.hpus > 0)
        config.model.params.hpu_graph = opt.hpu_graph
        if opt.use_autocast:
            config.model.params.use_autocast = opt.use_autocast
        config.model.params.accumulate_grad_batches = lightning_config.trainer.accumulate_grad_batches
        model = instantiate_from_config(config.model)

        # trainer and callbacks
        trainer_kwargs = dict()

        # default logger configs
        default_logger_cfgs = {
            "wandb": {
                "target": LIGHTNING_PACK_NAME + ".loggers.WandbLogger",
                "params": {
                    "name": nowname,
                    "save_dir": logdir,
                    "offline": opt.debug,
                    "id": nowname,
                }
            },
            "tensorboard": {
                "target": LIGHTNING_PACK_NAME + "loggers.TensorBoardLogger",
                "params": {
                    "name": "tensorboard",
                    "save_dir": logdir,
                }
            },
        }

        if opt.tensorboard and opt.wandb:
            raise ValueError("Chose any one logger Tensorboard or Wandb, not both!!!")
        if opt.tensorboard:
            default_logger_cfg = default_logger_cfgs["tensorboard"]
        elif opt.wandb:
            default_logger_cfg = default_logger_cfgs["wandb"]
        else:
            print("No logger enabled!!!")
            default_logger_cfg = None
        if "logger" in lightning_config:
            logger_cfg = lightning_config.logger
        else:
            logger_cfg = OmegaConf.create()

        if default_logger_cfg is not None:
            logger_cfg = OmegaConf.merge(default_logger_cfg, logger_cfg)
            trainer_kwargs["logger"] = instantiate_from_config(logger_cfg)

        # modelcheckpoint - use TrainResult/EvalResult(checkpoint_on=metric) to
        # specify which metric is used to determine best models
        default_modelckpt_cfg = {
            "target": LIGHTNING_PACK_NAME + "callbacks.ModelCheckpoint",
            "params": {
                "dirpath": ckptdir,
                "filename": "{epoch:06}",
                "verbose": True,
                "save_last": True,
            }
        }
        if hasattr(model, "monitor"):
            print(f"Monitoring {model.monitor} as checkpoint metric.")
            default_modelckpt_cfg["params"]["monitor"] = model.monitor
            default_modelckpt_cfg["params"]["save_top_k"] = 3

        if "modelcheckpoint" in lightning_config:
            modelckpt_cfg = lightning_config.modelcheckpoint
        else:
            modelckpt_cfg =  OmegaConf.create()
        modelckpt_cfg = OmegaConf.merge(default_modelckpt_cfg, modelckpt_cfg)
        if not opt.no_ckpt:
            print(f"Merged modelckpt-cfg: \n{modelckpt_cfg}")

        # add callback which sets up log directory
        default_callbacks_cfg = {
            "setup_callback": {
                "target": "main.SetupCallback",
                "params": {
                    "resume": opt.resume,
                    "now": now,
                    "logdir": logdir,
                    "ckptdir": ckptdir,
                    "cfgdir": cfgdir,
                    "config": config,
                    "lightning_config": lightning_config,
                    "debug": opt.debug,
                    "no_ckpt": opt.no_ckpt,
                }
            },
            "learning_rate_logger": {
                "target": "main.LearningRateMonitor",
                "params": {
                    "logging_interval": "step",
                    # "log_momentum": True
                }
            },
        }
        if "gpus" in trainer_config:
            default_callbacks_cfg.update({
                "cuda_callback": {
                    "target": "main.CUDACallback"
                },
            })
        elif opt.hpus:
            default_callbacks_cfg.update({
                "hpu_callback": {
                    "target": "main.HPUCallback"
                },
            })
        if opt.no_ckpt:
            trainer_kwargs["enable_checkpointing"] = False
        else:
            default_callbacks_cfg.update({'checkpoint_callback': modelckpt_cfg})

        if "print_freq" in config:
            print_freq_config = {
            "target": LIGHTNING_PACK_NAME + "callbacks.TQDMProgressBar",
            "params": {
                "refresh_rate": config.print_freq.refresh_rate,
                }
            }
            default_callbacks_cfg.update({'print_freq': print_freq_config})

        if "callbacks" in lightning_config:
            callbacks_cfg = lightning_config.callbacks
        else:
            callbacks_cfg = OmegaConf.create()

        if 'metrics_over_trainsteps_checkpoint' in callbacks_cfg:
            print(
                'Caution: Saving checkpoints every n train steps without deleting. This might require some free space.')
            default_metrics_over_trainsteps_ckpt_dict = {
                'metrics_over_trainsteps_checkpoint':
                    {"target": LIGHTNING_PACK_NAME + "callbacks.ModelCheckpoint",
                     'params': {
                         "dirpath": os.path.join(ckptdir, 'trainstep_checkpoints'),
                         "filename": "{epoch:06}-{step:09}",
                         "verbose": True,
                         'save_top_k': -1,
                         'every_n_train_steps': 10000,
                         'save_weights_only': True
                     }
                     }
            }
            default_callbacks_cfg.update(default_metrics_over_trainsteps_ckpt_dict)

        callbacks_cfg = OmegaConf.merge(default_callbacks_cfg, callbacks_cfg)
        if 'ignore_keys_callback' in callbacks_cfg and hasattr(trainer_opt, 'resume_from_checkpoint'):
            callbacks_cfg.ignore_keys_callback.params['ckpt_path'] = trainer_opt.resume_from_checkpoint
        elif 'ignore_keys_callback' in callbacks_cfg:
            del callbacks_cfg['ignore_keys_callback']

        trainer_kwargs["callbacks"] = [instantiate_from_config(callbacks_cfg[k]) for k in callbacks_cfg]

        if not "plugins" in trainer_kwargs:
            trainer_kwargs["plugins"] = None
        if not "strategy" in trainer_kwargs:
            trainer_kwargs["strategy"] = None

        if opt.hpus > 1:
            from lightning_habana.pytorch.strategies import HPUParallelStrategy
            from lightning_habana.pytorch.accelerator import HPUAccelerator
            parallel_hpus = [torch.device("hpu")] * trainer_config["devices"]
            dist._DEFAULT_FIRST_BUCKET_BYTES = 600*1024*1024  #600MB
            trainer_kwargs["strategy"] = HPUParallelStrategy(parallel_devices=parallel_hpus, broadcast_buffers=False, find_unused_parameters=False, bucket_cap_mb=600, gradient_as_bucket_view=True)
            trainer_kwargs["accelerator"] = HPUAccelerator()
        elif opt.hpus == 1:
            from lightning_habana.pytorch.strategies import SingleHPUStrategy
            from lightning_habana.pytorch.accelerator import HPUAccelerator
            trainer_kwargs["strategy"] = SingleHPUStrategy()
            trainer_kwargs["accelerator"] = HPUAccelerator()
        elif not lightning_config.get("find_unused_parameters", True):
            from lightning.pytorch.plugins import DDPPlugin
            trainer_kwargs["plugins"] = DDPPlugin(find_unused_parameters=False)

        if MULTINODE_HACKS:
            # disable resume from hpc ckpts
            # NOTE below only works in later versions
            # from pytorch_lightning.plugins.environments import SLURMEnvironment
            # trainer_kwargs["plugins"].append(SLURMEnvironment(auto_requeue=False))
            # hence we monkey patch things
            from lightning.pytorch.trainer.connectors.checkpoint_connector import _CheckpointConnector
            setattr(_CheckpointConnector, "hpc_resume_path", None)

        assert ("accelerator" in trainer_kwargs) or ("accelerator" in trainer_config)

        trainer = Trainer(**vars(trainer_opt), **trainer_kwargs)
        trainer.logdir = logdir  ###


        if len(opt.dataset_path):
            config.data.params.tar_base = opt.dataset_path
        data = instantiate_from_config(config.data)
        # NOTE according to https://pytorch-lightning.readthedocs.io/en/latest/datamodules.html
        # calling these ourselves should not be necessary but it is.
        # lightning still takes care of proper multiprocessing though
        data.prepare_data()
        data.setup("fit")
        print("#### Data #####")
        try:
            for k in data.datasets:
                print(f"{k}, {data.datasets[k].__class__.__name__}, {len(data.datasets[k])}")
        except:
            print("datasets not yet initialized.")

        # configure learning rate
        bs, base_lr = config.data.params.batch_size, config.model.base_learning_rate
        if not cpu and "gpus" in trainer_config:
            ngpu = len(lightning_config.trainer.gpus.strip(",").split(','))
        elif cpu:
            ngpu = 1
        elif opt.hpus:
            ngpu = trainer_config["devices"]
        if 'accumulate_grad_batches' in lightning_config.trainer:
            accumulate_grad_batches = lightning_config.trainer.accumulate_grad_batches
        else:
            accumulate_grad_batches = 1
        print(f"accumulate_grad_batches = {accumulate_grad_batches}")
        lightning_config.trainer.accumulate_grad_batches = accumulate_grad_batches
        config.model.params.accumulate_grad_batches = accumulate_grad_batches
        if opt.scale_lr:
            model.learning_rate = accumulate_grad_batches * ngpu * bs * base_lr
            print(
                "Setting learning rate to {:.2e} = {} (accumulate_grad_batches) * {} (num_gpus) * {} (batchsize) * {:.2e} (base_lr)".format(
                    model.learning_rate, accumulate_grad_batches, ngpu, bs, base_lr))
        else:
            model.learning_rate = base_lr
            print("++++ NOT USING LR SCALING ++++")
            print(f"Setting learning rate to {model.learning_rate:.2e}")


        # allow checkpointing via USR1
        def melk(*args, **kwargs):
            # run all checkpoint hooks
            if trainer.global_rank == 0 and not opt.no_ckpt:
                print("Summoning checkpoint.")
                ckpt_path = os.path.join(ckptdir, "last.ckpt")
                trainer.save_checkpoint(ckpt_path)


        def divein(*args, **kwargs):
            if trainer.global_rank == 0:
                import pudb;
                pudb.set_trace()


        import signal

        signal.signal(signal.SIGUSR1, melk)
        signal.signal(signal.SIGUSR2, divein)

        # run
        if opt.train:
            try:
                trainer.fit(model, data, ckpt_path=opt.resume_from_checkpoint)
            except Exception:
                if not opt.debug:
                    melk()
                raise
        if not opt.no_test and not trainer.interrupted:
            trainer.test(model, data)
    except RuntimeError as err:
        if MULTINODE_HACKS:
            import requests
            import datetime
            import os
            import socket
            device = os.environ.get("CUDA_VISIBLE_DEVICES", "?")
            hostname = socket.gethostname()
            ts = datetime.datetime.utcnow().strftime('%Y-%m-%d %H:%M:%S')
            resp = requests.get('http://169.254.169.254/latest/meta-data/instance-id')
            print(f'ERROR at {ts} on {hostname}/{resp.text} (CUDA_VISIBLE_DEVICES={device}): {type(err).__name__}: {err}', flush=True)
        raise err
    except Exception:
        if opt.debug and trainer.global_rank == 0:
            try:
                import pudb as debugger
            except ImportError:
                import pdb as debugger
            debugger.post_mortem()
        raise
    finally:
        # move newly created debug project to debug_runs
        if opt.debug and not opt.resume and trainer.global_rank == 0:
            dst, name = os.path.split(logdir)
            dst = os.path.join(dst, "debug_runs", name)
            os.makedirs(os.path.split(dst)[0], exist_ok=True)
            os.rename(logdir, dst)
        if trainer.global_rank == 0:
            print(trainer.profiler.summary())
